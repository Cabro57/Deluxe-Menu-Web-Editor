/**
 * Returns a URL for the texture of the given material via PrismarineJS minecraft-assets.
 * URL format: /data/{version}/items/{name}.png or /data/{version}/blocks/{name}.png
 * @param {string} material - Material name
 * @param {boolean} isBlock - Whether to use block texture (default: try item first)
 */
export function getTextureUrl(material, isBlock = false) {
    if (!material) return null;

    const m = material.toUpperCase();
    const cleanName = m.toLowerCase().replace(/ /g, "_");

    // PrismarineJS minecraft-assets repository
    const baseUrl = "https://raw.githubusercontent.com/PrismarineJS/minecraft-assets/master/data/1.20.2";

    // Check special textures first
    if (SPECIAL_TEXTURES[m]) {
        // Special textures might be in blocks folder
        return `${baseUrl}/${SPECIAL_TEXTURES[m].replace('block/', 'blocks/').replace('item/', 'items/').replace('.png', '')}.png`;
    }

    // Return item or block texture based on parameter
    const folder = isBlock ? "blocks" : "items";
    return `${baseUrl}/${folder}/${cleanName}.png`;
}

/**
 * Returns a block texture URL specifically
 */
export function getBlockTextureUrl(material) {
    return getTextureUrl(material, true);
}


/**
 * Returns a URL for the 3D head image of the given material string if it matches a head format.
 * Supports: head-, basehead-, texture-, hdb-
 */
export function getHeadUrl(material) {
    if (!material) return null;

    // Player Name
    if (material.startsWith("head-")) {
        const name = material.substring(5);
        return `https://mc-heads.net/head/${name}`;
    }

    // Base64 Value
    if (material.startsWith("basehead-")) {
        try {
            const base64 = material.substring(9);
            const decoded = JSON.parse(atob(base64));
            const url = decoded?.textures?.SKIN?.url;
            if (url) {
                const hash = url.split('/').pop();
                return `https://mc-heads.net/head/${hash}`;
            }
        } catch (e) {
            console.warn("Failed to decode basehead:", e);
            // If decode fails, checking if the substring itself is a hash/uuid is a hail mary, 
            // but for base64 it usually means invalid data.
        }
        return `https://mc-heads.net/head/Steve`; // Fallback
    }

    // Texture URL / Hash
    if (material.startsWith("texture-")) {
        const val = material.substring(8);
        // If it's a full URL, extract hash
        if (val.includes("textures.minecraft.net")) {
            const hash = val.split('/').pop();
            return `https://mc-heads.net/head/${hash}`;
        }
        // Assume it's a hash if no URL structure
        return `https://mc-heads.net/head/${val}`;
    }

    // Head Database ID - placeholder as we don't have direct HDB API
    if (material.startsWith("hdb-")) {
        // HDB IDs don't map directly to textures without the plugin database.
        // Showing a generic "Head" icon or Steve for now.
        return `https://mc-heads.net/head/Steve`;
    }

    return null;
}

export function searchMaterials(query) {
    if (!query) return MATERIALS;
    const lower = query.toLowerCase();
    return MATERIALS.filter(m => m.toLowerCase().includes(lower));
}
